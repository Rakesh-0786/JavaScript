// With the HTML DOM , JavaScript can access and changes all the elements of an HTML document.

// The HTML DOM(Document Object Model)
// When a web page is  loaded, the browser creates Document Object Model of the page.

// The HTML DOM model is  constructed as a tree of Objects.

// With the object Model, JavaScript gets all the power it needs to create dynamic HTML:
// JavaScript can change all the HTML elements in the page.
// JavaScript can Change all the HTML attributes in the page.
// JavaScript can remove existing HTML elements and attributs
// JavaScript can add new HTMl elements and attributes 
// javaScript can react to all existing HTML events in the page.
// javaScript can create new HTML events in the page


// What is DOM?
// The DOM is a W3C (World Wide Web Consortium) standard.

// The DOM defines a standard for accessing documents:

// "The W3C Document Object Model (DOM) is a platform and language-neutral interface that allows programs and scripts to dynamically access and update the content, structure, and style of a document."


// The W3C DOM standard is a separated into 3 different parts:
// Core DOM -Standard model for all document types
// XML DOM-standard model for XML documents
// HTML DOM-Standard model for HTML documents


// What is the HTML DOM?
// The HTML DOM is a standard object model and programming interface for HTML.
// IT defines:
// THe html elements as objects
// the properties of all HTML elements
// the methods to access all HTML elements
// the events for all HTML elements

// In other words: THE HTML DOM is a standard for how to get, change,add, or, delete HTML elements.
